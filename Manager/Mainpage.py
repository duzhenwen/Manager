         self.system.pack_forget()
         self.yourself.pack_forget()
         self.add_class.pack_forget()
         self.delete_teacher.pack_forget()
         self.delete_class.pack_forget()
         self.query_teacher.pack_forget()
         self.query_class.pack_forget()
         self.add_teacher.pack_forget()
         self.add_teacher.pack()
         
